def render_phase4_execution_plane(target: Path) -> list[Path]:
    written: list[Path] = []
    for skill in SKILL_PACK:
        written.append(safe_write(target, f"skills/{skill['name']}/SKILL.md", make_skill_md(skill)))
    rituals_doc = {
        "version": "2.0.0-phase4",
        "doctrine": "Agents propose. Humans judge. Nurses steward.",
        "mode": "templates_only_not_scheduled",
        "note": "These are reviewable cron templates. They are not installed into Hermes cron automatically.",
        "safety": {
            "no_phi": True,
            "clinical_decisions": False,
            "delivery": "local_or_user_configured_gateway_only",
            "requires_human_review_before_activation": True,
        },
        "rituals": RITUALS,
    }
    written.append(safe_write(target, "cron/rituals.yaml", yaml.safe_dump(rituals_doc, sort_keys=False, allow_unicode=True)))
    written.append(safe_write(target, "cron/README.md", "# NAIO Cron Ritual Templates\n\nThese are Phase 4 templates only. They are not scheduled automatically. Review each prompt, choose delivery explicitly, and never include PHI.\n\nAgents propose. Humans judge. Nurses steward.\n"))
    for ritual in RITUALS:
        prompt_name = ritual["prompt_file"].replace("cron/prompts/", "")
        written.append(safe_write(target, f"cron/prompts/{prompt_name}", make_ritual_prompt(ritual)))
    return written

def render_runtime(soul: dict, projects: dict | None) -> dict:
    ceilings = soul.get("tier_ceilings", {})
    spheres = soul.get("spheres", list(ceilings.keys()))
    runtime = {
        "version": "2.0.0-phase4",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "doctrine": "Agents propose. Humans judge. Nurses steward.",
        "mode": "generated-profile-bundle",
        "mutation_scope": "target-directory-only",
        "sphere_ceilings": {},
        "toolset_mapping": {},
        "human_gates": {},
        "permission_scoping": {
            "default": "read_only",
            "least_privilege": True,
            "complete_mediation": True,
            "irreversible_actions": "draft_then_human_executes",
        },
        "projects": [],
        "skills": [
            {
                "name": s["name"],
                "tier": s["tier"],
                "sphere": s["sphere"],
                "human_gate": GATE_BY_TIER[s["tier"]],
                "path": f"skills/{s['name']}/SKILL.md",
            }
            for s in SKILL_PACK
        ],
        "cron_rituals": [
            {
                "id": r["id"],
                "schedule": r["schedule"],
                "tier": r["tier"],
                "sphere": r["sphere"],
                "skill": r["skill"],
                "prompt_file": r["prompt_file"],
                "scheduled": False,
            }
            for r in RITUALS
        ],
    }
    for sphere in spheres:
        tier = ceilings.get(sphere, "green")
        if tier not in ALLOWED_TIERS:
            refuse(f"invalid sphere tier after validation: {sphere}={tier}")
        runtime["sphere_ceilings"][sphere] = tier
        runtime["toolset_mapping"][sphere] = TOOLSETS_BY_TIER[tier]
        runtime["human_gates"][sphere] = GATE_BY_TIER[tier]
    for p in (projects or {}).get("projects", []):
        runtime["projects"].append({
            "name": p.get("name"),
            "sphere": p.get("sphere"),
            "tier": p.get("edena_tier"),
            "human_gate": p.get("human_gate"),
            "path": f"projects/{p.get('slug') or slugify(p.get('name','project'))}.SYSTEM.md",
        })
    return runtime

def main() -> int:
    ap = argparse.ArgumentParser(description="Render a governed Hermes-ready profile bundle.")
    ap.add_argument("--soul", required=True, help="path to naio-soul.json")
    ap.add_argument("--projects", default=None, help="path to naio-projects.json")
    ap.add_argument("--target", required=True, help="target output directory")
    ap.add_argument("--force", action="store_true", help="overwrite existing target")
    args = ap.parse_args()

    soul_path = Path(args.soul)
    if not soul_path.is_file():
        refuse(f"SOUL file not found: {soul_path}")
    soul = load_json(soul_path)
    projects = load_json(Path(args.projects)) if args.projects else None
    validate_soul(soul)
    validate_projects(projects)

    target = Path(args.target).expanduser().resolve()
    if target.exists():
        if not args.force:
            refuse(f"target already exists; pass --force to replace: {target}")
        if target == Path.home() or str(target) in ("/", str(Path.home() / ".hermes")):
            refuse("refusing to overwrite home or ~/.hermes directly")
        shutil.rmtree(target)
    target.mkdir(parents=True, exist_ok=True)

    written = []
    written.append(safe_write(target, "README-FIRST.md", textwrap.dedent(f"""
    # NAIO Hermes Profile Bundle — Phase 4

    This folder was generated from your SOUL Quiz and Life & Projects Quiz exports.

    **Nothing was written to ~/.hermes.** This is a governed, reviewable profile bundle.
    Copy/adapt it only after review.

    ## What is inside
    - `SOUL.md` — core identity and boundaries.
    - `spheres/` — per-sphere SOUL files with EDENA ceilings and gates.
    - `projects/` — project system prompts from Life & Projects.
    - `skills/` — tier-tagged starter skills with EDENA frontmatter.
    - `cron/` — stewardship ritual templates, not auto-scheduled.
    - `config/edena-runtime.yaml` — computed tier → gate/toolset/skill/ritual mapping.
    - `config/human-gates.yaml` — gate behavior summary.
    - `config/hermes-profile.patch.yaml` — suggested Hermes config overlay, not auto-applied.

    ## Boundary
    No PHI. No clinical decisions. No irreversible external action without human review.

    Agents propose. Humans judge. Nurses steward.
    """).strip() + "\n"))
    written.append(safe_write(target, "SOUL.md", make_core_soul(soul)))

    for sphere, tier in soul.get("tier_ceilings", {}).items():
        written.append(safe_write(target, f"spheres/{sphere}.SOUL.md", make_sphere_soul(sphere, tier)))

    if projects:
        for p in projects.get("projects", []):
            slug = p.get("slug") or slugify(p.get("name", "project"))
            written.append(safe_write(target, f"projects/{slug}.SYSTEM.md", make_project_prompt(p)))

    written.extend(render_phase4_execution_plane(target))

    runtime = render_runtime(soul, projects)
    written.append(safe_write(target, "config/edena-runtime.yaml", yaml.safe_dump(runtime, sort_keys=False, allow_unicode=True)))
    gates = {
        "version": "2.0.0-phase4",
        "non_removable_for": ["green", "yellow"],
        "gates": {
            "every-output": "Nurse reviews every generated output before use.",
            "before-external-use": "Nurse reviews before sending, publishing, sharing, or operational use.",
            "forced-irreversible": "Nurse executes irreversible actions manually; AI drafts only.",
        },
        "anti_rubber_stamp": "Reserve gates for meaning; show effect before asking for approval.",
    }
    written.append(safe_write(target, "config/human-gates.yaml", yaml.safe_dump(gates, sort_keys=False, allow_unicode=True)))
    patch = {
        "profile_name": "naio-generated-review-before-use",
        "soul_file": "SOUL.md",
        "project_context": ".hermes.md or selected project SYSTEM.md",
        "enabled_toolsets_by_sphere": runtime["toolset_mapping"],
        "human_gates": runtime["human_gates"],
        "note": "Suggested overlay only. Do not paste into ~/.hermes/config.yaml without review.",
    }
    written.append(safe_write(target, "config/hermes-profile.patch.yaml", yaml.safe_dump(patch, sort_keys=False, allow_unicode=True)))
    written.append(safe_write(target, ".hermes.md", "# NAIO Project Rules\n\nLoad SOUL.md first. Apply EDENA runtime gates. No PHI. No clinical decisions. Agents propose. Humans judge. Nurses steward.\n"))

    report = {
        "generated_at": runtime["generated_at"],
        "target": str(target),
        "files_written": [str(p.relative_to(target)) for p in written],
        "sha256": {str(p.relative_to(target)): hashlib.sha256(p.read_bytes()).hexdigest() for p in written},
    }
    written.append(safe_write(target, "logs/render-report.json", json.dumps(report, indent=2) + "\n"))
    print(f"✅ Phase 3 profile bundle rendered: {target}")
    print(f"   files_written={len(written)}")
    print("   mutation_scope=target-directory-only")
    return 0

if __name__ == "__main__":
    try:
        sys.exit(main())
    except SystemExit:
        raise
    except Exception as e:
        print(f"❌ INTERNAL ERROR: {e}", file=sys.stderr)
        sys.exit(1)
